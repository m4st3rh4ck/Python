"""                      Ejercicio
Crea la función restar que dependa de tres parámetros a, b y c. 
Esta función al ser llamada debe mostrar el resultado de la resta 
de los tres valores ingresados."""

def restar(a, b ,c): #a,b,c son numeros
    print(a - b - c)

restar(10, 5, 3)
restar(3, 5, 2)
restar(9, 4, 1)
